package model;

public class DrawerItem {
	private String drawerMenus;
	private int drawerImage;
	public String getDrawerMenus() {
		return drawerMenus;
	}
	public void setDrawerMenus(String drawerMenus) {
		this.drawerMenus = drawerMenus;
	}
	public int getDrawerImage() {
		return drawerImage;
	}
	public void setDrawerImage(int drawerImage) {
		this.drawerImage = drawerImage;
	}
	
}
